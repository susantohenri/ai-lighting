          <p class="text-justify">
            The purpose of this is for the customer to understand the value of their discount.
            For the first few projects ai will provide you the exact terminology and help you though this process.
          </p>
          <br>
          <p class="bold">BCA Evidence</p>
          <p class="text-justify">
            The BCA evidence is incredibly important and unless it is correct, the whole claim can fall over or be devalued depending on the evaluated use of the building.
          </p>
          <p class="text-justify">
            Firstly, not all high bay upgrades are valued equally. Different BCA classifications of the premises determine what the estimated annual operating hours are. There are three main categories that are relevant to high bay installations. 2000, 3000 and 5000 hours.
          </p>
          <p class="text-justify">
            You are not expected to be an expert in this assessment, ai lighting will work with you to determine this. In the ‘High bays and Pricing’ page, there is an ‘Application’ column that will cover off most of the possible installations.
          </p>
          <p class="text-justify">
            The BCA evidence collection can be challenging, and it is advised to have this completed before agreeing to or undertaking the installation.
          </p>
          <p class="text-justify">
            The most comprehensive evidence that covers off all the annual operating hours categories is a rental lease. The rental lease will contain the permitted use which is accepted by the regulator.
          </p>
          <p class="text-justify">
            For retail businesses we can accept a dedicated web page or photos of the shop front that clearly show the stores opening hours. This will only be accepted if it is reviewed and approved by ai lighting prior to the job.
          </p>
          <br>
          <p class="bold">
            Photographic Evidence
          </p>
          <p class="text-justify">
            The single most important part of the photographic evidence is ensuring that your smartphone’s settings are enabled to allow location data to be stored in the photos. This is called geotagging and the data in the photo is called meta data. Please check your settings and test to ensure that every photo contains date/ time stamp and GPS coordinates. This is a mandatory requirement and can cause a claim to fail if not available. Ai lighting will work with you to transfer the photos ensuring the data remains intact.
          </p>
          <p class="text-justify">
            Before and after photos are required from each upgrade space, showing as many of the luminaires as possible.
Each Luminaire, Lamp, Ballast and control device must be <b>photographed from the installation site</b> clearly displaying all the text on each item.
          </p>
          <p class="text-justify">
            A Photo of the removed lighting in a pile with all power leads cut off must be provided to prove decommissioning.
          </p>
          <br>
          <p class="bold">Floor Plans</p>
          <p class="text-justify">
            Baseline and upgrade plans must be provided for every job. These can be hand drawn and photographed to be submitted with the rest of the evidence.
          </p>

        </div>
      </div>
      <br>
      <br>
    </div>
