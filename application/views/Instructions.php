          <h4 class="bold">LED Highbay VEET Upgrade Process</h4>      
        </div>
        <div class="col-xs-3 pull-right">
          <img src="<?= base_url('asset/img/ai-lighting-150.png') ?>" style="width:80px;padding:5px" alt="" class="pull-right">
