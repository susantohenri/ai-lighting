          <p class="text-justify">
            Ai Lighting reserves the right to reject any project if the order does not pass an internal risk assessment.
          </p>
          <p class="text-justify">
            Ai Lighting is effectively granting credit to projects. To minimise the financial risk the Installing Electrician and the Customer must co-sign a Purchase order / Project authorisation document that accepts:
          </p>
          <ul>
            <li>
              <p>The job is authorised to proceed</p>
            </li>
            <li>
              <p>The customer/ energy consumer understands the scope of works that is occurring.</p>
            </li>
            <li>
              <p>Financial liability of the stock if the project is not completed and VEECS are not
created. Or,</p>
            </li>
            <li>
              <p>Grants ai Lighting permission take control of completing a project on behalf of the
energy consumer if the installing electrician fails to meet timeframe and installation
performance.</p>
            </li>
            <li>
              <p>Grants ai Lighting permission to collect stock that isn’t installed</p>
            </li>
          </ul>

          <p class="text-justify">It is in the best interest of all parties that any problem is resolved before any action is taken. The document protects against criminal activity and ultimately protects the energy consumer.</p>
          <p class="text-justify">Depending on the size and location of the job dispatch method of materials will be determined case by case.</p>
          <br>
          <p class="bold">Drop off/ Collection of the removed lighting.</p>
          <p class="text-justify">Every single lamp and ballast must be retained for recycling. Ai Lighting will work with you to take possession of the old lighting. Depending on the size of the job, location and how busy we are, we may be able to collect the old lighting. We will work with you to get the lighting to our Narre Warren Location where it will be processed at the Hampton Park Transfer Station.</p>
          <p class="text-justify">
            If the number of returned lights does not match the claim, the claim will be reduced to match the recycling evidence. Ai Lighting takes care of this step and ensure the spent lighting is disposed off responsibly and the correct documentation is collected in the form of an itemised recycling receipt. A claim cannot be processed without this evidence.
          </p>
          <br>
          <p class="bold">
            Processing Time and Payments.
          </p>
          <p class="text-justify">
            Processing times vary. There is lots of work in the background that occurs to create the VEECS that eventually turn into revenue that pays for your work. Claims are processed in the order that they are received. You will be advised prior to commencement of the job if there is any extended lead time on processing. Generally, the claim is processed within a few days of receiving it. If there is information missing you will be sent a RFI to complete. The sooner we get the information the sooner we get the claim in. If documentation is not received within a timely manner it can increase the risk of VEEC rise and fall and the estimated amount of money you earn may change. Once a claim has been approved, a Purchase order will be issued to you with the final amount. Once invoiced, it will be paid between 5-10 business days.
          </p>
