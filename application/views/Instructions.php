      <br>
      <div class="row">
        <div class="col-sm-offset-1 col-sm-10">
          <p class="text-justify">
            The evidence collection and paperwork component of a VEET lighting upgrade is not as complicated as many seem to think. If you spend the time to understand the process and follow these guidelines, it will add less than 20 minutes to the average Project.
          </p>
          <p class="text-justify">
            The documentation is broken down into the following components
          </p>
          <ul>
            <li><p class="bold">Assignment Form and Data Collection Sheet</p></li>
            <li><p class="bold">Customer Invoice and Certificate of Electrical Safety</p></li>
            <li><p class="bold">BCA Evidence</p></li>
            <li><p class="bold">Photographic Evidence</p></li>
            <li><p class="bold">Floor Plans</p></li>
          </ul>
          <p class="text-justify">
            This instructional document will go through each point. At the end of the document will be an example of a completed job for reference. For quick reference you may wish to print this document, or you can save this pdf document to your mobile device in iBook’s or an equivalent Android document viewer.
          </p>
          <br>
          <p class="bold">Assignment Form and Data Collection Sheet</p>
          <p class="text-justify">
            This document is to be completed by hand from site. Please print text and ensure it is clearly readable. This is a self-explanatory step and can be mostly completed by the customer them selves while you are working. The check list provided should be marked off progressively to not miss an evidence step. e.g. Forgetting the baseline photos before the fittings are removed can invalidate the VEET claim altogether.
          </p>
          <br>
          <p class="bold">
            Customer Invoice and Certificate of Electrical Safety
          </p>
          <p class="text-justify">
            These two items are incidental with any electrical job and will generally occur off site. VEET requirements mandate very particular details to be included on both documents. If they are missed, the claim will be held up until the correct amendments are made and the documents are resubmitted.
          </p>
          <p>
            Key details that are usually not required for average jobs are
          </p>
          <ul>
            <li>
              <p>Customers ABN to be listed on the invoice under the recipient details.</p>
            </li>
            <li>
              <p>Make and model of the upgrade lighting to be detailed on the CES</p>
            </li>
            <li>
              <p>Details of any control device added on both invoice ad CES</p>
            </li>
            <li>
              <p>Accurate completion date of the project on the CES</p>
            </li>
            <li>
              <p>VEET Discount provided to customer</p>
            </li>
          </ul>
          <p class="text-justify">
            The VEET discount is the agreed ‘Installed Payback Price’ multiplied by the number of fittings upgraded as a part of the project. If the revenue generated is enough to do the job as a zero out of pocket cost to the customer, the invoice should display a $0.00 total
          </p>
          <p class="text-justify">
            If the revenue provides a discount only and a co payment is required, the balance amount needs to be visible on the invoice and becomes a functional invoice that the customer will be required to settle.
          </p>
          <br>
          <br>
        </div>
      </div>
