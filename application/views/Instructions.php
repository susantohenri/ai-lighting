        <div class="col-sm-offset-1 col-md-10">
          <p class="text-justify">
            Graph paper has been included in the document pack for this Purpose.
          </p>
          <p class="text-justify">
            We are flexible on how the plans are drawn if the data is accurate clearly understood. Once the hand drawn plans are submitted to us, we digitise the drawings using floor plan software and lighting design simulations are then conducted on the computer-generated plan. AS1680 lighting standards are assessed against the output.
<br>The key details we need in the plan are:
          </p>
          <ul>
            <li>
              <p>Shape and dimensions of the space</p>
            </li>
            <li>
              <p>Baseline lights in their existing locations</p>
            </li>
            <li>
              <p>Ceiling or luminaire mounting heights</p>
            </li>
            <li>
              <p>Distinguishing features of the building like roller doors and entries. This helps us orientate our selves in the space when referring to the evidence photos.</p>
            </li>
            <li>
              <p>Any changes to the layout/ new luminaire locations</p>
            </li>
            <li>
              <p>Location of air conditioning vents if any</p>
            </li>
          </ul>
          <p class="bold">Submission to ai Lighting.</p>
          <p class="text-justify">
            Every section included in the document pack can be photographed individually and uploaded to a shared folder via the Google Photos application. Photos of the supporting documents like Customer Invoice, CES, and sign off form can be photographed off site at your home or office. The geo tagging of these photos are not important. Only lamp/ control gear and space photos require GPS data that match the installation address.
          </p>
          <p class="text-justify">
            Google Photos is a universal application that provides a simple way to share your project documents reliably without damaging the meta-data contained within the mandatory site photos.
          </p>
          <p class="text-justify">
            To share your photos with ai Lighting you must have a Google account. If you don’t have a Google account, you can create one by clicking this link -> <a target="_blank" href="https://accounts.google.com/signup/v2/webcreateaccount?continue=https%3A%2F%2Fplay.google.com%2Fstore%2Fapps%2Fdetails%3Fid%3Dcom.google.android.apps.photos%26referrer%3Dutm_source%253Dphotosweb%2526utm_medium%253Dpromo%2526utm_term%253Dempty&flowName=GlifWebSignIn&flowEntry=SignUp"><img src="<?= base_url('asset/img/google.png') ?>" style="height:20px" alt=""></a>.
